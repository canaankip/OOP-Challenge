from pet import Pet

# Create a pet object
buddy = Pet("Buddy")

# Show initial status
buddy.get_status()

# Feed the pet
buddy.eat()

# Play with the pet
buddy.play()

# Put the pet to sleep
buddy.sleep()

# Teach the pet a trick
buddy.train("roll over")

# Show learned tricks
buddy.show_tricks()

# Final status
buddy.get_status()

